KEY_A = [0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF]



def load_key(connection):
    # Load key into reader slot 0
    load_key_cmd = [0xFF, 0x82, 0x00, 0x00, 0x06] + KEY_A
    response, sw1, sw2 = connection.transmit(load_key_cmd)
    if (sw1, sw2) != (0x90, 0x00):
        raise Exception("Failed to load key")
    print("Key loaded successfully")


def authenticate(connection, block):
    auth_cmd = [0xFF, 0x86, 0x00, 0x00, 0x05,
                0x01, 0x00, block, 0x60, 0x00]
    response, sw1, sw2 = connection.transmit(auth_cmd)
    return (sw1, sw2) == (0x90, 0x00)


def write_block(connection, block_num, data):
    cmd = [0xFF, 0xD6, 0x00, block_num, 0x10] + data
    response, sw1, sw2 = connection.transmit(cmd)
    print(f"Block {block_num}: {sw1:02X} {sw2:02X}")


def write_all_blocks(reader_index):
    r = readers()[reader_index]
    connection = r.createConnection()
    connection.connect()

    load_key(connection)
    if authenticate(connection, 0):
        print("Auth success with this key")
    else:
        print("Auth failed with this key")
    input()
    # Write block 0 (UID block - only works on special cards)
    write_block(connection, 0, BLOCK_DATA[0])

    for sector in range(16):  # MIFARE Classic 1K = 16 sectors
        first_block = sector * 4

        # Authenticate once per sector
        if not authenticate(connection, first_block):
            print(f"Auth failed for sector {sector}")
            continue

        for i in range(4):
            block_num = first_block + i

            # ⚠️ Skip sector trailer
            if block_num % 4 == 3:
                continue

            write_block(connection, block_num, BLOCK_DATA[block_num])


